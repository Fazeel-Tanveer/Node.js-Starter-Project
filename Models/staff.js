const mongoose = require("mongoose");

const staffSchema = new mongoose.Schema({
  name: String,
  email: {
		type: String,
		lowercase: true,
		match: /[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*@(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?/,
  },
  password: String,
  phone_number: String,
  image: String
});

module.exports = mongoose.model("Staff", staffSchema);